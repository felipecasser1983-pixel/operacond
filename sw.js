/* =====================================================================
   SERVICE WORKER — OperaCond
   Faz cache de todos os arquivos do app no momento da instalação, para
   que o aplicativo abra e funcione 100% offline mesmo sem nenhuma
   conexão de rede, depois de visitado/instalado uma primeira vez.
   ===================================================================== */
const CACHE_NAME = 'operacond-cache-v1';
const ASSETS = [
  './',
  './index.html',
  './manifest.json',
  './icons/icon-192.png',
  './icons/icon-512.png'
];

self.addEventListener('install', (event) => {
  event.waitUntil(
    caches.open(CACHE_NAME).then((cache) => cache.addAll(ASSETS))
  );
  self.skipWaiting();
});

self.addEventListener('activate', (event) => {
  event.waitUntil(
    caches.keys().then((keys) =>
      Promise.all(keys.filter((k) => k !== CACHE_NAME).map((k) => caches.delete(k)))
    )
  );
  self.clients.claim();
});

/* Estratégia "cache-first": tenta servir do cache; se não houver, busca na rede
   e guarda no cache para uso offline futuro. */
self.addEventListener('fetch', (event) => {
  event.respondWith(
    caches.match(event.request).then((cached) => {
      if (cached) return cached;
      return fetch(event.request)
        .then((response) => {
          const clone = response.clone();
          caches.open(CACHE_NAME).then((cache) => cache.put(event.request, clone));
          return response;
        })
        .catch(() => cached);
    })
  );
});
